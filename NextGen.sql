
use hospital;
CREATE TABLE INVOICES (INVOICE_ID INT NOT NULL,
                       AMOUNT_ID INT NOT NULL,
                       PRIMARY KEY (INVOICE_ID));
CREATE TABLE PATIENTS (PATIENT_ID INT NOT NULL,
                       FIRST_NAME VARCHAR (50) NOT NULL,
                       LAST_NAME VARCHAR (50) NOT NULL,
                       PHONE_NO VARCHAR (10) NOT NULL,
                       EMAIL_ID VARCHAR (30) NOT NULL,
                       DOB_DATE date NOT NULL,
                       PRIMARY KEY (PATIENT_ID));
CREATE TABLE INSURENCE (INSURENCE_ID INT NOT NULL,
                       POLICY_NUM VARCHAR (20) NOT NULL,
                       DISCOUNT INT NOT NULL,
                       PLAN VARCHAR (50) NOT NULL,
                       PRIMARY KEY (INSURENCE_ID));
CREATE TABLE PHARMACIES (PHARMACY_ID INT NOT NULL,
                       NAME VARCHAR (100) NOT NULL,
                       PRIMARY KEY (PHARMACY_ID));
CREATE TABLE ADDRESS_INFO (ADDRESS_ID INT NOT NULL,
                       PATIENT_ID INT NOT NULL,
                       COMPANY_ID INT NOT NULL,
                       PHARMACY_ID INT NOT NULL,
                       PRIMARY KEY (ADDRESS_ID),
                       FOREIGN KEY PATIENT(PATIENT_ID) REFERENCES PATIENTS(PATIENT_ID),
                       FOREIGN KEY COMAPNY(COMPANY_ID) REFERENCES COMPANIES(COMPANY_ID),
                       FOREIGN KEY PHARMACY(PHARMACY_ID) REFERENCES PHARMACIES(PHARMACY_ID));
CREATE TABLE PRESCRIPTION_INFO (PRESCRIPTION_ID INT NOT NULL,
                       PHARMACY_ID INT NOT NULL,
                       VISIT_ID INT NOT NULL,
                       DOCTOR_ID INT NOT NULL,
                       PRIMARY KEY (PRESCRIPTION_ID),
                       FOREIGN KEY PHARMACY(PHARMACY_ID) REFERENCES PHARMACIES(PHARMACY_ID),
                       FOREIGN KEY VISIT(VISIT_ID) REFERENCES VISIT_INFO(VISIT_ID),
                       FOREIGN KEY DOCTOR(DOCTOR_ID) REFERENCES DOCTOR(DOCTOR_ID));
CREATE TABLE VISIT_INFO (VISIT_ID INT NOT NULL,
                       DOCTOR_ID INT NOT NULL,
                       PATIENT_ID INT NOT NULL,
                       INVOICE_ID INT NOT NULL,
                       VISIT_DATE DATE NOT NULL,
                       PRIMARY KEY (VISIT_ID),
                       FOREIGN KEY DOCTOR(DOCTOR_ID) REFERENCES DOCTOR(DOCTOR_ID),
                       FOREIGN KEY PATIENT(PATIENT_ID) REFERENCES PATIENTS(PATIENT_ID),
                       FOREIGN KEY INVOICE(INVOICE_ID) REFERENCES INVOICES(INVOICE_ID));
CREATE TABLE CLIAMS (PATIENT_ID INT NOT NULL,
						INSURENCE_ID INT NOT NULL,
						PRIMARY KEY(PATIENT_ID, INSURENCE_ID));
CREATE TABLE INSURENCE_COMPANIES (INSURENCE_ID INT NOT NULL,
                                  COMPANY_ID INT NOT NULL,
								PRIMARY KEY(INSURENCE_ID, COMPANY_ID));
							
